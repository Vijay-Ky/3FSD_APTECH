class C {

	public static void main(String[] args) {
		System.out.println(456123);
		System.out.println(456123 / 10);//removing one digit by 10
		System.out.println(456123 / 100);//removing two digits by 100
		System.out.println(456123 / 1000);//removing three digits by 1000
		System.out.println(456123 / 10000);//removing four digits by 10000
		System.out.println(456123 / 100000);//removing five digits by 100000
		System.out.println(456123 / 1000000);//removing six digits by 1000000
	}
}
